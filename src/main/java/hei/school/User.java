package hei.school;

public class User {
    private int idUser;
    private String name;
    private String dateOfBirth;
    private String email;

    public User(int idUser, String name, String dateOfBirth, String email) {
        this.idUser = idUser;
        this.name = name;
        this.dateOfBirth = dateOfBirth;
        this.email = email;
    }
}
